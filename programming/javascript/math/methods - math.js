console.log(Math.round(4.5));   // Returns x rounded to nearest integer, 5
console.log(Math.ceil(4.2));    // Returns x rounded up to nearest integer, 5
console.log(Math.floor(4.9));   // Returns x rounded down to nearest integer, 4
console.log(Math.trunc(4.7));   // Returns the integer part of x, 4

console.log(Math.sign(-5));     // Returns if x is negative, zero or positive (returns -1, 0 or 1), -1
console.log(Math.abs(-5));      // Returns absolute value of x, 5

console.log(Math.pow(2, 3));    // Returns value of x to the power of y, 8
console.log(Math.sqrt(25));     // Returns square root of x, 5
console.log(Math.cbrt(27));     // Returns square root of x, 3
console.log(Math.log(12));      // Returns natural (base e) log of x, 2.485
console.log(Math.log2(12));     // Returns base 2 log of x, 3.585
console.log(Math.log10(12));    // Returns base 10 log of x, 1.079

console.log(Math.min(5, 1, 9)); // Returns lowest value in list of arguments, 1
console.log(Math.max(5, 1, 9)); // Returns highest value in list of arguments, 9

console.log(Math.sin(Math.PI)); // Returns sine of x in radians
console.log(Math.cos(Math.PI)); // Returns cosine of x in radians
console.log(Math.tan(Math.PI)); // Returns tangent of x in radians

console.log(Math.random());     // Returns a random number between 0 inclusive and 1 exclusive
